/** Toán tử gán

Toán tử     Ví dụ       Tương đương
=           x = y       x = y
+=          x += y      x = x + y
-=          x -= y      x = x - y
*=          x *= y      x = x * y
/=          x /= y      x = x / y
**=         x **= y     x = x ** y  
 */

var a = 1;
a += 2;
console.log(a); // a = a + 2 = 3

var b = 2;
b **= 2;
console.log(b); // b = b ** 2 = 4

